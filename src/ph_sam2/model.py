def map_centroids_to_original(img_gray, c1, c2):
    """
    c1 = centroid from non-flipped crop
    c2 = centroid from vertically flipped crop
    map both to original image coordinates
    """
    h, w = img_gray.shape[:2]
    top    = int(h * TOP_RATIO)
    bottom = int(h * (1 - BOTTOM_RATIO))
    left   = int(w * LEFT_RATIO)
    right  = int(w * (1 - RIGHT_RATIO))
    crop_h = bottom - top

    x1o = left + int(c1[0])
    y1o = top  + int(c1[1])

    y2_unflipped = (crop_h - 1) - int(c2[1])
    x2o = left + int(c2[0])
    y2o = top  + y2_unflipped

    x1o = np.clip(x1o, 0, w-1); y1o = np.clip(y1o, 0, h-1)
    x2o = np.clip(x2o, 0, w-1); y2o = np.clip(y2o, 0, h-1)
    return (int(x1o), int(y1o)), (int(x2o), int(y2o))

def dice_score_bin(pred_mask_bin, gt_mask_bin):
    a = pred_mask_bin.astype(bool)
    b = gt_mask_bin.astype(bool)
    inter = np.logical_and(a, b).sum()
    denom = a.sum() + b.sum()
    return 1.0 if denom == 0 else (2.0 * inter / (denom + 1e-8))

def find_gt_path(base_id, original_filename):
    exts = [".png", ".jpg", ".jpeg", ".bmp", ".tif", ".tiff"]

    # 1) exact same filename first
    candidate = os.path.join(GT_DIR, original_filename)
    if os.path.exists(candidate):
        return candidate

    # 2) base + _mask.<ext>
    for e in exts:
        candidate = os.path.join(GT_DIR, f"{base_id}_mask{e}")
        if os.path.exists(candidate):
            return candidate

    # 3) base + any ext
    for e in exts:
        candidate = os.path.join(GT_DIR, f"{base_id}{e}")
        if os.path.exists(candidate):
            return candidate

    return None

def load_sam2_predictor(model_cfg: str, checkpoint_path: str):
    sam2_model = build_sam2(model_cfg, checkpoint_path, device=DEVICE)
    predictor = SAM2ImagePredictor(sam2_model)
    return predictor

def segment_sam2_on_original(predictor, image_bgr, p1, p2, mask_index:int):
    """
    predictor.set_image expects BGR in your notebook — keep consistent.
    returns (mask_bin 0/1, mask_img 0/255)
    """
    predictor.set_image(image_bgr)
    pts = np.array([p1, p2], dtype=np.float32)
    labels = np.array([1, 1], dtype=np.int32)

    masks, scores, logits = predictor.predict(
        point_coords=pts,
        point_labels=labels,
        multimask_output=True
    )  # masks: [K,H,W]

    masks = np.asarray(masks)
    if masks.dtype != np.bool_:
        masks = masks > 0.5

    if masks.shape[0] == 0:
        raise RuntimeError("SAM2 returned zero masks")

    # pad to at least 3 masks (some configs can return <3)
    K = masks.shape[0]
    if K < 3:
        masks = np.concatenate([masks, np.repeat(masks[-1][None,...], 3-K, axis=0)], axis=0)

    if mask_index >= masks.shape[0]:
        raise RuntimeError(f"Requested mask_index={mask_index} but got K={masks.shape[0]} masks")

    chosen = masks[mask_index]
    mask_bin = chosen.astype(np.uint8)
    mask_img = (mask_bin * 255).astype(np.uint8)
    return mask_bin, mask_img

