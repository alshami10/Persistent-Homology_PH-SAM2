def run_phase1_phsam2_one_model(model_key, cfg, sampled_files, save_masks=True):
    model_out_dir = os.path.join(OUT_ROOT, f"{model_key}_mask{cfg['mask_idx']}_seed{SEED}")
    os.makedirs(model_out_dir, exist_ok=True)

    print(f"\n=== Running {model_key} (mask {cfg['mask_idx']}) ===")
    predictor = load_sam2_predictor(cfg["cfg"], cfg["ckpt"])

    rows = []
    dice_list = []
    time_list = []

    t_batch0 = time.time()

    for filename in sampled_files:
        img_path = os.path.join(ORIG_IMAGE_DIR, filename)
        base_id = os.path.splitext(filename)[0]
        gt_path = find_gt_path(base_id, filename)

        # read image
        image_bgr = cv2.imread(img_path, cv2.IMREAD_COLOR)
        if image_bgr is None:
            continue
        g_gray = cv2.cvtColor(image_bgr, cv2.COLOR_BGR2GRAY)

        # centroids (crop coords)
        c1 = tuple(map(int, centroids[filename][0]))
        c2 = tuple(map(int, centroids[filename][1]))
        p1, p2 = map_centroids_to_original(g_gray, c1, c2)

        # SAM2 inference + timing
        t0 = time.time()
        pred_bin, pred_img = segment_sam2_on_original(
            predictor, image_bgr, p1, p2, mask_index=cfg["mask_idx"]
        )
        t1 = time.time()
        dt = t1 - t0

        # load GT bin
        gt = cv2.imread(gt_path, cv2.IMREAD_UNCHANGED)
        if gt is None:
            continue
        if gt.ndim == 3:
            gt_bin = (np.sum(gt, axis=2) > 0).astype(np.uint8)
        else:
            gt_bin = (gt > 0).astype(np.uint8)

        # resize GT if needed
        if gt_bin.shape != pred_bin.shape:
            gt_bin = cv2.resize(gt_bin, (pred_bin.shape[1], pred_bin.shape[0]), interpolation=cv2.INTER_NEAREST)

        d = dice_score_bin(pred_bin, gt_bin)

        dice_list.append(d)
        time_list.append(dt)

        pred_name = ""
        if save_masks:
            pred_name = f"{base_id}_pred_{model_key}_mask{cfg['mask_idx']}_dice{d:.4f}.png"
            cv2.imwrite(os.path.join(model_out_dir, pred_name), pred_img)

        rows.append({
            "filename": filename,
            "id": base_id,
            "gt_file": os.path.basename(gt_path),
            "dice": float(d),
            "time_sec": float(dt),
            "pred_mask_file": pred_name
        })

    t_batch1 = time.time()
    batch_time = t_batch1 - t_batch0

    avg_dice = float(np.mean(dice_list)) if dice_list else 0.0
    best_dice = float(np.max(dice_list)) if dice_list else 0.0
    avg_time = float(np.mean(time_list)) if time_list else 0.0

    # Per-image CSV
    per_csv = os.path.join(model_out_dir, f"phase1_{model_key}_mask{cfg['mask_idx']}_seed{SEED}.csv")
    with open(per_csv, "w", newline="") as f:
        w = csv.writer(f)
        w.writerow(["filename","id","gt_file","dice","time_sec","pred_mask_file"])
        for r in rows:
            w.writerow([r["filename"], r["id"], r["gt_file"],
                        f"{r['dice']:.6f}", f"{r['time_sec']:.6f}", r["pred_mask_file"]])
        w.writerow([])
        w.writerow(["SUMMARY","","","","",""])
        w.writerow(["avg_dice", avg_dice])
        w.writerow(["best_dice", best_dice])
        w.writerow(["avg_time_per_image_sec", avg_time])
        w.writerow(["total_batch_time_sec", float(batch_time)])
        w.writerow(["n_images_used", len(rows)])

    summary = {
        "model": model_key,
        "mask_index": cfg["mask_idx"],
        "seed": SEED,
        "n_images_used": len(rows),
        "avg_dice": avg_dice,
        "best_dice": best_dice,
        "avg_time_per_image_sec": avg_time,
        "total_batch_time_sec": float(batch_time),
        "csv_path": per_csv,
        "out_dir": model_out_dir
    }

    print("DONE:", summary)
    return summary

